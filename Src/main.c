/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	static uint16_t eeMLX90640[832];
	uint16_t frame[834];
	float Ta,tr;
	float emissivity=0.95;
	static float mlx90640To[768];
	u16 i=0;
	struct Upload upload;
  /* USER CODE END 1 */
  

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI2_Init();
  /* USER CODE BEGIN 2 */
	network_init();
	Delay_Init(84);


	MLX90640_SetRefreshRate(MLX_I2C_ADDR, RefreshRate);
	MLX90640_SetChessMode(MLX_I2C_ADDR);
	paramsMLX90640 mlx90640;
	MLX90640_DumpEE(MLX_I2C_ADDR, eeMLX90640);
	MLX90640_ExtractParameters(eeMLX90640, &mlx90640);

	for(i=0;i<3;i++)//Lose the start frame
	{
			MLX90640_GetFrameData(MLX_I2C_ADDR, frame);
			delay_ms(500);
	}
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
		MLX90640_GetFrameData(MLX_I2C_ADDR, frame);
		Ta = MLX90640_GetTa(frame, &mlx90640);
		tr = Ta - TA_SHIFT;
		MLX90640_CalculateTo(frame, &mlx90640, emissivity, tr, mlx90640To);



		for(i=0;i<768;i++)//hundredfold,first send low 8bit and then high 8bit,DMA circular transfer
		{
				upload.target_temperature[i] = (unsigned short)(mlx90640To[i]*100);
		}

		upload.self_temperature = (unsigned short)(Ta*100);


		sendto(0, (uint8_t *)&upload, sizeof(struct Upload), net_work.desip, DHCP_CLIENT_PORT);
  }
  /* USER CODE END 3 */
}

